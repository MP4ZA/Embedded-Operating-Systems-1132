void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size) {
	int dis=0;
  if (huart == &huart3 && Size > 0) {

	//HAL_UART_Transmit_DMA(huart, receiveData, Size);

    /*
    if(strcmp((char*)receiveData, "1")==0){
    	HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_13);//Orange
    }
    else if(strcmp((char*)receiveData, "2")==0){
    	HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_14);//Red
    }
    else{
    	HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_15);//Blue
    */
    switch (BT_INFO.BT_state){
		case WAIT_MODE:
			if(strcmp((char*)receiveData, BlueTooth_WORK)==0){
				SwitchMode(WORK_MODE);
			}
			break;
		case WORK_MODE:
			if(strcmp((char*)receiveData, BlueTooth_WAIT)==0){
				SwitchMode(WAIT_MODE);
			}
			else if(strcmp((char*)receiveData, Red_LED_TOGGLE)==0){//Red
				HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_14);
			}
			else if(strcmp((char*)receiveData, ORANGE_FLASHING)==0){//orange
				if(ORANGE_LED_==1)ORANGE_LED_= 0;
				else if(ORANGE_LED_==0)ORANGE_LED_= 1;
			}
			else if(strcmp((char*)receiveData, BlueTooth_FIND)==0){
				SwitchMode(FIND_MODE);
			}
			break;
		case FIND_MODE:
			if(strcmp((char*)receiveData, BlueTooth_WORK)==0){
				Buzz_=0;
				SwitchMode(WORK_MODE);
			}
			else if(strcmp((char*)receiveData, BUZZ_ON)==0){
				Buzz_=1;
			}
			else if(strcmp((char*)receiveData, BUZZ_OFF)==0){
				Buzz_=0;
				HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, GPIO_PIN_SET);
			}
			else if(strncmp((char*)receiveData, "RSSI",4)==0){
				dis = ((char*)receiveData)[5]*10 + ((char*)receiveData)[6]- 528;
				BT_INFO.BT_distance = dis;
			}
			break;
		default:
			break;
	}

    HAL_UARTEx_ReceiveToIdle_DMA(huart, receiveData, sizeof(receiveData));
    __HAL_DMA_DISABLE_IT(&hdma_usart3_rx, DMA_IT_HT);
  }
}
