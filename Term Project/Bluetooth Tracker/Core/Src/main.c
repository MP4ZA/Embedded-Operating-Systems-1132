void Buzz(int dis){
	if(Buzz_){
		if(dis>10){
			fre = (dis-10)*5+10;
		}
		else {fre=dis;}
		HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_15);
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_9, GPIO_PIN_RESET);//Buzz on
		HAL_Delay(fre);
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_9, GPIO_PIN_SET);//Buzz off
		HAL_Delay(fre);

	}
}
void Orange_LED(){
	if(ORANGE_LED_){
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, GPIO_PIN_SET);
		HAL_Delay(200);
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, GPIO_PIN_RESET);
		HAL_Delay(200);
	}
}
